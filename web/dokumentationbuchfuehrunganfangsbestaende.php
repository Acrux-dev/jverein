    <h1>Dokumentation: Buchführung: Anfangsbestände</h1>
    <h2>Allgemeines</h2>
	<p>Für jedes Konto muss zum Beginn des Geschäftsjahres der Anfangsbestand gebucht werden. Wird ein Konto 
		im Laufe des Jahres eröffnet, ist der Bestand zu diesem Zeitpunkt zu buchen.</p>
	<p>Kontobestände können nur in zeitlich aufsteigender Reihenfolge eingegeben werden. Wurde beispielsweise 
		zu einem Konto der Bestand zum 1.1.2008 eingegeben, kann nicht mehr für den 1.1.2007 gebucht werden.</p>
	<p>Anfangsbestände, die durch einen <a href="dokumentationbuchfuehrungjahresabschluss.php">Jahresabschluss</a>
		erfasst sind, können nicht mehr geändert oder gelöscht werden. Außerdem können keine neuen 
		Anfangsbestände für abgeschlossene Zeiträume eingegeben werden.</p>
	<h2>Auflistung der Anfangsbestände</h2>
	<img src='images/Anfangsbestandsliste.jpg' class='screenshot'>
	<p>Durch neu werden neue Anfangsbestände aufgenommen. Mit einem Doppelklick auf einen Anfangsbestand 
		kann der Bestand bearbeitet werden. Ein Rechtsklick auf einen Anfangsbestand öffnet ein Kontextmenü. 
		Damit kann ein Anfangsbestand gelöscht werden.</p>
